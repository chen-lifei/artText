    family_arr () {
        let family_arr = [];
        for (let i = 0, family = font_family.family; i < family.length; i++) {
            for (let j = 0, item = family[i].item; j < item.length; j++) {
                family_arr = family_arr.concat(item[j].item);
            }
        }
        return family_arr;
    },
    // 精确搜索字体
    search_exact (val) {
        let result = false;
        if (typeof val !== 'string') return result;
        let font = val.replace(/[\'\"]/g, '').toLocaleLowerCase();
        let family_arr = font_family.family_arr();
        // 查找对应的字体
        for (let index = 0; index < family_arr.length; index++) {
            if (font === family_arr[index].name.toLocaleLowerCase() || font === family_arr[index].data.toLocaleLowerCase()) {
                result = family_arr[index];
                break;
            }
            // 有子集时同时查找子集
            if (family_arr[index].childrenList) {
                let list = family_arr[index].childrenList;
                let flag = false;
                for (let i = 0; i < list.length; i++) {
                    if (font === list[i].name.toLocaleLowerCase() || font === list[i].data.toLocaleLowerCase()) {
                        result = list[i];
                        flag = true;
                    }
                    if(flag === true) break;
                }
            }
        }
        return result;
    },
    // 模糊搜索字体
    search_dim (val) {
        let result = [];
        if (typeof val !== 'string') return result;
        let font = val.replace(/[\'\"]/g, '').toLocaleLowerCase();
        let family_arr = font_family.family_arr();
        // 查找对应的字体
        for (let index = 0; index < family_arr.length; index++) {
            if (font === family_arr[index].name.toLocaleLowerCase() || font === family_arr[index].data.toLocaleLowerCase()) {
                result.push(family_arr[index]);
            }
            // 有子集时同时查找子集
            if (family_arr[index].childrenList) {
                let list = family_arr[index].childrenList;
                let flag = false;
                for (let i = 0; i < list.length; i++) {
                    if (font === list[i].name.toLocaleLowerCase() || font === list[i].data.toLocaleLowerCase()) {
                        flag = true;
                    }
                    if(flag === true) result.push(list[i]);
                }
            }
        }
        return result;
    },
